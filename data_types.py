print([1, 2, 3, 4, 5])   #similar to array in other languages and always use square brackets
print(["raj", "saloni", "nikhil"])

#tuples
print((1, 2, 3, 4, 5))   #similar to array in other languages and always use round brackets

#sets
print({1, 2, 3, 4, 5})   #similar to array in other languages and always use curly brackets

#dictionary
print({"name": "raj", "age": 20, "city": "mumbai"})   #similar to array in other languages and always use curly brackets have a key value pair.


